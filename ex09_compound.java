public class ex09_compound {
    public static void main(String[] args) {
        int x= 10, y = 5;
        x += y;
        System.out.println(x);
        x -= y;
        System.out.println(x);
        x *= y;
        System.out.println(x);
        x /= y;
        System.out.println(x);
        x %= y;
        System.out.println(x);
    }
}
